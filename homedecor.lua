			local tab = tonumber(fields.info)
			minetest.show_formspec(player_name, 
						"stock_exchange:shop", shop_formspec(tab))
		elseif fields.quit ~= "true" then
			local name,val = next(fields)
			player:set_attribute("shop_item", name)
			local desc = ItemLookup[name]
			minetest.show_formspec(player_name, "stock_exchange:shop_buy", buy_formspec(name, desc))
		end
	elseif formname == "stock_exchange:shop_buy" then
		if fields.buy == "Kaufen" then
			local number = fields.number or "1"
			number = tonumber(number) or 1
			number = math.min(number, 99)
			local price = PRICE * number
			local shop_item = player:get_attribute("shop_item")
			if stock_exchange.player_has_money(player_name, price) then
				if stock_exchange.add_to_players_inventory(player_name, shop_item, number) then
					stock_exchange.update_player_hud(player_name, -price)
				end
			end
		end
	end
end


